def write_chapter(idx):
    title = TITLES[(idx - 1) % 10]
    scenes = SCENES.get(title, [])
    scene_idx = (idx - 1) % len(scenes) if scenes else 0
    body = scenes[scene_idx] if scenes else [f'{title}。', '今天第一次。']
    cn_num = _num_to_chinese(idx)
    header = f'第{cn_num}回 · {title}'
    content = header + '\n\n' + '\n'.join(body) + '\n'
    fname = f'ch{idx:03d}-{title}.md'
    with open(fname, 'w', encoding='utf-8', newline='') as f:
        f.write(content)
    return fname


def _num_to_chinese(n):
    """Return Chinese numeral WITHOUT the 回 suffix (caller adds it)."""
    digits = '〇一二三四五六七八九'
    if n < 10:
        return digits[n]
    if n < 20:
        return '十' + (digits[n-10] if n > 10 else '')
    if n < 100:
        tens, ones = n // 10, n % 10
        s = digits[tens] + '十'
        if ones:
            s += digits[ones]
        return s
    if n < 1000:
        h, r = n // 100, n % 100
        s = digits[h] + '百'
        if r == 0:
            return s
        if r < 10:
            return s + '零' + digits[r]
        if r < 20:
            return s + '一十' + (digits[r-10] if r > 10 else '')
        t, o = r // 10, r % 10
        s += digits[t] + '十'
        if o:
            s += digits[o]
        return s
    raise ValueError(n)


